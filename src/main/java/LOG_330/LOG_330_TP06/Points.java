package LOG_330.LOG_330_TP06;

/**
 * Class Points takes 2 attributes X and Y
 * @author Walid
 *
 */
public class Points {
	
	private Double x;
	private Double y;
	
	public Points(Double x, Double y) {
		this.x = x;
		this.y = y;
	}
	
	public Double getX() {
		return this.x;
	}
	
	public Double getY() {
		return this.y;
	}

}
